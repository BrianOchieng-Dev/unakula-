export async function chatWithAssistant(
  message: string,
  context: string
): Promise<string> {
  if (!message.trim()) {
    return "Ask me about a meal combo, budget, or what to eat around campus.";
  }

  if (containsRestrictedContent(message)) {
    return "That request is not allowed here. This app is for student food discussions only.";
  }

  try {
    const response = await ai.models.generateContent({
      model: "gemini-3-flash-preview",
      contents: [
        {
          role: "user",
          parts: [
            {
              text: message,
            },
          ],
        },
      ],
      config: {
        systemInstruction: `You are the Ulikula? assistant for MMUST students.
Your goal is to help students decide what to eat around campus.

Recent meal context:
${context}

Rules:
- Be concise, friendly, and useful.
- Suggest realistic student meals like ugali, sukuma wiki, beans, chapati, rice, omena, samaki, mandazi, chai, and kibandaski options.
- Use light Kenyan campus vibe naturally, without overdoing slang.
- Never engage with sexual, explicit, or body-related requests.
- If a request is inappropriate, politely refuse and redirect to food-related help.`,
      },
    });

    const text = extractText(response);
    return text ?? "I'm not sure yet. Ask me about a meal, budget, or location.";
  } catch (error) {
    console.error("Chat error:", error);
    return "I hit a problem on my side. Ask again in a simpler way.";
  }
}