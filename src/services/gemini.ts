function containsRestrictedContent(input: string): boolean {
  const lower = input.toLowerCase();
  return RESTRICTED_WORDS.some((word) => lower.includes(word));
}

function buildFallbackFoodImage(mealCombo: string): string {
  return `https://picsum.photos/seed/${encodeURIComponent(mealCombo)}/800/800`;
}

function buildFallbackAvatar(fullname: string): string {
  return `https://api.dicebear.com/7.x/avataaars/svg?seed=${encodeURIComponent(fullname)}`;
}

function extractInlineImageData(response: unknown): string | null {
  const candidates =
    (response as {
      candidates?: Array<{
        content?: {
          parts?: Array<{
            inlineData?: { data?: string; mimeType?: string };
            text?: string;
          }>;
        };
      }>;
    }).candidates ?? [];

  for (const candidate of candidates) {
    const parts = candidate.content?.parts ?? [];
    for (const part of parts) {
      const base64 = part.inlineData?.data;
      const mimeType = part.inlineData?.mimeType ?? "image/png";

      if (base64) {
        return `data:${mimeType};base64,${base64}`;
      }
    }
  }

  return null;
}

function extractText(response: unknown): string | null {
  const typed = response as {
    text?: string;
    candidates?: Array<{
      content?: {
        parts?: Array<{ text?: string }>;
      };
    }>;
  };

  if (typeof typed.text === "string" && typed.text.trim()) {
    return typed.text.trim();
  }

  const candidates = typed.candidates ?? [];
  for (const candidate of candidates) {
    const parts = candidate.content?.parts ?? [];
    for (const part of parts) {
      if (typeof part.text === "string" && part.text.trim()) {
        return part.text.trim();
      }
    }
  }

  return null;
}

